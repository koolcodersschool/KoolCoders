package day2_intro_to_variables;

public class VariableDeclaration {
    public static void main(String[] args) {
        // Declare single variable
        int apples;
        // Declare multiple variables in one statement
        int bananas, oranges, mangoes, lemons;
        // Declare single variable and assign value in one statement
        int kiwi = 4;
        // Declare and assign value to multiple variables in single statement
        int watermelon = 34, grapes = 90, cherries = 20, apricots = 0;
    }
}
